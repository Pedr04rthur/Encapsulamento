package revisando.lacos;

public class RepBasica {
    public static void main(String[] args){
            //para yma variável que inicia em 1, e vai até 10,
        //mudando de 1-por-1, faça:
        for (int i = 1; i <= 10; i++ ){
            for (int j = 1; j <= 10; j++ ){
                System.out.println(i + " X " + j + " = "+ i * j);
            }
        }
    }
}
